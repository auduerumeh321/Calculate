class Calculate99{

double addition(int number10,int number12){

return number10+number12;

}

double substraction(int number5,int number4){

return number5-number4;

}

public static void main(String[]args){
Calculate99 calculate = new Calculate99();

System.out.print(calculate.addition(10,12));

System.out.print(calculate.substraction(5,4));

}

}
